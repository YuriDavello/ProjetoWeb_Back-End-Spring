package com.ifsp.pwe_projetofinal.model;

public class Teste {

    private String testeUm;
    private String testeDois;

    public Teste() {
    }

    public Teste(String testeUm, String testeDois) {
        this.testeUm = testeUm;
        this.testeDois = testeDois;
    }

    public String getTesteUm() {
        return testeUm;
    }

    public void setTesteUm(String testeUm) {
        this.testeUm = testeUm;
    }

    public String getTesteDois() {
        return testeDois;
    }

    public void setTesteDois(String testeDois) {
        this.testeDois = testeDois;
    }
}
